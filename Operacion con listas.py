#Operaciones con listas
#Obtener un elemento
lenguaje_progra=['python','Ruby','Java','C++','JavaScript']
#Obtener un elemento
print(lenguaje_progra[0])
print((lenguaje_progra[2]))

#Dividir una lista
print(lenguaje_progra[0:3])
print(lenguaje_progra[:3])
print(lenguaje_progra[2:4])
print(lenguaje_progra[::-1])#Invertir una lista

#Invertir una lista
print(lenguaje_progra[::-1]) #Setp negativo, conservando la lista inicial
print(lenguaje_progra)
reversed(lenguaje_progra)#Invertir una lista con la funcion reversed
print(lenguaje_progra)
lenguaje_progra.reverse()#Invertir una lista, pero afectando a la original
print(lenguaje_progra)
